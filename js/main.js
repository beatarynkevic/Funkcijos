/**
 * Funkcija skirta sumuoti
 * @param {number} a pirmasis skaicius
 * @param {number} b antrasis skaicius
 * @returns {number} grazina suma dvieju skaitmenu
 */
function sum(a, b) {
    return a + b;
}

console.log(sum(2, 3));

function multiplication(a, b) {
    return a * b;
}

console.log(multiplication(4, 5));